function populateCountrySelect(selectId = 'countrySelect') {
    const select = document.getElementById(selectId);
    if (!select) {
        console.error(`Select element with id '${selectId}' not found`);
        return;
    }
    
    const firstOption = select.querySelector('option[disabled]');
    select.innerHTML = '';
    if (firstOption) {
        select.appendChild(firstOption);
    }
    const sortedCountries = countries.sort((a, b) => a.name.localeCompare(b.name));
    
    sortedCountries.forEach(country => {
        const option = document.createElement('option');
        option.value = country.code;
        option.textContent = country.name;
        select.appendChild(option);
    });
}

function createSearchableCountrySelect(containerId) {
    const container = document.getElementById(containerId);
    if (!container) {
        console.error(`Container element with id '${containerId}' not found`);
        return;
    }
    container.innerHTML = `
        <div class="searchable-select">
            <input type="text" id="countrySearch" placeholder="Search country..." class="country-search-input">
            <div class="country-dropdown" id="countryDropdown">
                <!-- Countries will be populated here -->
            </div>
            <input type="hidden" name="country" id="selectedCountry">
        </div>
        <div id="quintanaRooQuestion" class="quintana-roo-question" style="display: none;">
            <label class="question-label">Are you a resident of Quintana Roo?</label>
            <div class="checkbox-options">
                <label class="checkbox-option">
                    <input type="radio" name="quintanaRooResident" value="yes" id="quintanaRooYes">
                    <span class="checkbox-text">Yes</span>
                </label>
                <label class="checkbox-option">
                    <input type="radio" name="quintanaRooResident" value="no" id="quintanaRooNo">
                    <span class="checkbox-text">No</span>
                </label>
            </div>
        </div>
    `;

    
    const searchInput = document.getElementById('countrySearch');
    const dropdown = document.getElementById('countryDropdown');
    const hiddenInput = document.getElementById('selectedCountry');
    const quintanaRooQuestion = document.getElementById('quintanaRooQuestion');
    const sortedCountries = countries.sort((a, b) => a.name.localeCompare(b.name));
    

    function toggleQuintanaRooQuestion(countryCode) {
        if (countryCode === 'MX') {
            quintanaRooQuestion.style.display = 'block';
        } else {
            quintanaRooQuestion.style.display = 'none';

            const radios = quintanaRooQuestion.querySelectorAll('input[type="radio"]');
            radios.forEach(radio => radio.checked = false);
        }
    }
    
    function displayCountries(countriesToShow) {
        dropdown.innerHTML = '';
        countriesToShow.forEach(country => {
            const option = document.createElement('div');
            option.className = 'country-option';
            option.textContent = country.name;
            option.dataset.code = country.code;
            option.addEventListener('click', () => {
                searchInput.value = country.name;
                hiddenInput.value = country.code;
                dropdown.style.display = 'none';
                dropdown.innerHTML = '';
                
                toggleQuintanaRooQuestion(country.code);
            });
            dropdown.appendChild(option);
        });
    }

    searchInput.addEventListener('input', (e) => {
        const searchTerm = e.target.value.toLowerCase();
        if (searchTerm.length > 0) {
            const filteredCountries = sortedCountries.filter(country => 
                country.name.toLowerCase().includes(searchTerm)
            );
            displayCountries(filteredCountries);
            dropdown.style.display = 'block';
        } else {
            dropdown.style.display = 'none';
        }
    });
    
    searchInput.addEventListener('focus', () => {
        if (searchInput.value.length === 0) {
            displayCountries(sortedCountries);
            dropdown.style.display = 'block';
        }
    });
    
    document.addEventListener('click', (e) => {
        if (!container.contains(e.target)) {
            dropdown.style.display = 'none';
        }
    });

    const step3Data = getStepData(3); 
    if (step3Data && step3Data.country) {
        const selectedCountryName = getCountryName(step3Data.country);
        if (selectedCountryName) {
            searchInput.value = selectedCountryName;
            hiddenInput.value = step3Data.country;
            toggleQuintanaRooQuestion(step3Data.country);
            if (step3Data.quintanaRooResident) {
                const radioBtn = document.querySelector(`input[name="quintanaRooResident"][value="${step3Data.quintanaRooResident}"]`);
                if (radioBtn) {
                    radioBtn.checked = true;
                }
            }
        }
    }
}

function getCountryName(code) {
    const country = countries.find(c => c.code === code);
    return country ? country.name : null;
}

function getCountryCode(name) {
    const country = countries.find(c => c.name.toLowerCase() === name.toLowerCase());
    return country ? country.code : null;
}

window.populateCountrySelect = populateCountrySelect;
window.createSearchableCountrySelect = createSearchableCountrySelect;
window.getCountryName = getCountryName;
window.getCountryCode = getCountryCode;
window.countries = countries;
